/**
 * \defgroup clxx_info Clxx Info Objects
 *
 * This module documents structures used to store or pass over various
 * information retrieved from OpenCL. Although the data often describes
 * OpenCL things such as devices, platforms etc, the structures are though
 * to be independent on any OpenCL implementation.
 *
 * There are several use-cases for the info objects. Some examples include
 * data serialization (for example storing information about devices), data
 * I/O or passing over network in a distributed application.
 */
