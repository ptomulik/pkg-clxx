/**
 * \defgroup clxx_info libclxx_info
 *
 * \brief This module documents \ref clxx_info package
 *
 * This package contains structures used to store or pass over various
 * information retrieved from OpenCL. Although the data usually describes
 * OpenCL entities such as devices, platforms etc, the structures are though
 * to be independent on any OpenCL implementation. That way, they may be used
 * on platforms that have no OpenCL runtime library installed.
 *
 * There are several use-cases for the info objects. Some examples include
 * data serialization (for example storing information about devices), data
 * I/O or passing over network in a distributed application.
 */
